		struct KeyInfo
		{
			public int ki_key;
			public string ki_name;

			public KeyInfo(int ki_key, string ki_name)
			{
				this.ki_key=ki_key;
				this.ki_name=ki_name;
			}
		}

		static KeyInfo END_LIST=new KeyInfo(-1, "");

		static KeyInfo ValuePair(Enum token, object value)
		{
			KeyInfo k=new KeyInfo();
			k.ki_key=(int)(object)token;
			k.ki_name=token.ToString();
			return k;
		}

